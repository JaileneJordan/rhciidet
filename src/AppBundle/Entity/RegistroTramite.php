<?php

namespace AppBundle\Entity;

/**
 * RegistroTramite
 */
class RegistroTramite
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $fechaRegistro;

    /**
     * @var \AppBundle\Entity\Empleado
     */
    private $idEmpleado;

    /**
     * @var \AppBundle\Entity\Tramite
     */
    private $idTramite;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set fechaRegistro
     *
     * @param integer $fechaRegistro
     *
     * @return RegistroTramite
     */
    public function setFechaRegistro($fechaRegistro)
    {
        $this->fechaRegistro = $fechaRegistro;

        return $this;
    }

    /**
     * Get fechaRegistro
     *
     * @return integer
     */
    public function getFechaRegistro()
    {
        return $this->fechaRegistro;
    }

    /**
     * Set idEmpleado
     *
     * @param \AppBundle\Entity\Empleado $idEmpleado
     *
     * @return RegistroTramite
     */
    public function setIdEmpleado(\AppBundle\Entity\Empleado $idEmpleado = null)
    {
        $this->idEmpleado = $idEmpleado;

        return $this;
    }

    /**
     * Get idEmpleado
     *
     * @return \AppBundle\Entity\Empleado
     */
    public function getIdEmpleado()
    {
        return $this->idEmpleado;
    }

    /**
     * Set idTramite
     *
     * @param \AppBundle\Entity\Tramite $idTramite
     *
     * @return RegistroTramite
     */
    public function setIdTramite(\AppBundle\Entity\Tramite $idTramite = null)
    {
        $this->idTramite = $idTramite;

        return $this;
    }

    /**
     * Get idTramite
     *
     * @return \AppBundle\Entity\Tramite
     */
    public function getIdTramite()
    {
        return $this->idTramite;
    }
}
